// Fonction pour obtenir la langue initiale
const getInitialLanguage = () => {
  const savedLanguage = localStorage.getItem('language');
  const savedCountry = localStorage.getItem('country');
  
  if (savedLanguage) return savedLanguage;
  
  if (savedCountry) {
    const countryLanguageMap = {
      'CMF': 'fr-FR',
      'CMA': 'en-GB',
      'RCA': 'fr-FR',
      'TCD': 'fr-FR',
      'GNQ': 'es-ES',
      'BDI': 'en-GB',
      'COG': 'fr-FR'
    };
    return countryLanguageMap[savedCountry] || 'fr-FR';
  }
  
  return 'fr-FR';
};

const initialLanguage = getInitialLanguage();

// Initialiser i18n
i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: initialLanguage,
    fallbackLng: 'fr-FR',
    interpolation: {
      escapeValue: false,
    },
    react: {
      useSuspense: false,
    },
    // Options pour gérer les clés manquantes
    saveMissing: true,
    missingKeyHandler: (lng, ns, key, fallbackValue) => {
      console.warn(`Traduction manquante: ${key} pour la langue ${lng}`);
    }
  });

// Mettre à jour l'attribut lang du document HTML
document.documentElement.lang = initialLanguage;

// Fonction pour changer dynamiquement la langue
export const changeLanguage = (languageCode) => {
  i18n.changeLanguage(languageCode);
  localStorage.setItem('language', languageCode);
  document.documentElement.lang = languageCode;
};

